package com.dy.getresultapp;

public class AddedMoneyInfo {
    private double addedMoney;
    private double nowJz;
    private String nowDayStr;
    private double nowHasCount;

    public double getAddedMoney() {
        return addedMoney;
    }

    public void setAddedMoney(double addedMoney) {
        this.addedMoney = addedMoney;
    }

    public double getNowJz() {
        return nowJz;
    }

    public void setNowJz(double nowJz) {
        this.nowJz = nowJz;
    }

    public String getNowDayStr() {
        return nowDayStr;
    }

    public void setNowDayStr(String nowDayStr) {
        this.nowDayStr = nowDayStr;
    }

    public double getNowHasCount() {
        return nowHasCount;
    }

    public void setNowHasCount(double nowHasCount) {
        this.nowHasCount = nowHasCount;
    }
}
